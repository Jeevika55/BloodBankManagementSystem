    private void SubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitButtonActionPerformed
         Connector con=new Connector();
        con.createConnection();
       
        try {
            
            Statement stmt=Connector.conn.createStatement();
            PreparedStatement pst;
            String sql="Insert into orders(OrderId,BloodType,Quantity,Cost,HospitalId,Date) values(?,?,?,?,?,?)";
            pst=conn.prepareStatement(sql);
            pst.setString(1,OrderIdText.getText());
             pst.setString(2,Blood.getSelectedItem().toString());
              pst.setString(3,QuantityText.getText()); 
               pst.setString(4,CostText.getText());
               pst.setString(5,HospitalIdText.getText());
                pst.setString(6,DateText.getText());
            pst.execute();
            JOptionPane.showMessageDialog(null,"Order accepted");
            
            
            
            DefaultTableModel model=(DefaultTableModel) OrdersTable.getModel();
            Statement stmt1=Connector.conn.createStatement();
            String s="select * from orders where OrderId=(select max(OrderId) from orders);";
            ResultSet rs1=stmt1.executeQuery(s);
            while(rs1.next())
            {
                String orderid=rs1.getString("OrderId");
                String bloodtype=rs1.getString("BloodType");
                int quantity=rs1.getInt("Quantity");
                int cost=rs1.getInt("Cost");
                String hospitalid=rs1.getString("HospitalId");
                String date=rs1.getString("Date");
                model.addRow(new Object[] {orderid,bloodtype,quantity,cost,hospitalid,date});
            }
            rs1.close();
            stmt1.close();
            
        }
            
         catch(Exception e)
         {
             JOptionPane.showMessageDialog(null, e);
         }
    }//GEN-LAST:event_SubmitButtonActionPerformed

