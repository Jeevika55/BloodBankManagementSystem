    private void CostButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CostButtonActionPerformed

       try
        {
        
        Connector con=new Connector();
        con.createConnection();
        PreparedStatement ps;
        ps = Connector.conn.prepareStatement("SELECT cost from blood where bloodtype=?;");
            ps.setString(1, Blood.getSelectedItem().toString());
            ResultSet result = ps.executeQuery();
            while(result.next())
            {
                String q= QuantityText.getText();
                int quan=Integer.parseInt(q);
                String c=result.getString("cost");
                int cos=Integer.parseInt(c);
                int r=cos*quan;
                String res=String.valueOf(r);
                
                CostText.setText(res);
            }

          
        }
       catch(Exception e)
       {
           JOptionPane.showMessageDialog(null, e);
       }
       
       
       
    }//GEN-LAST:event_CostButtonActionPerformed

